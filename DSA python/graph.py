
class Point:
    def __init__(self, r: int, c: int):
        """
        r is the row index of a position in the board
        and c is the column index of a position in the board
        """
        self.r = r
        self.c = c

class Graph:
    def __init__(self, board: list[list[str]]):
        self.board = board
        self.m = len(board)
        self.n = len(board[-1])
    
    def findXs(self):
        """
        returns a list of all possible starting points in the
        board/graph
        """
        start_points = []
        for i in range(self.m):
            for j in range(self.n):
                if board[i][j] == 'X':
                    start_point = Point(i, j)
                    start_points.append(start_point)
        
        return start_points




